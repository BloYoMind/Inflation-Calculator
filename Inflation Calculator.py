import pandas as pd

def calculate_inflation_adjusted_value(start_value, start_year, end_year, cpi_data):
    """
    Calculates the inflation-adjusted value based on the CPI data.

    Parameters:
        start_value (float): The initial value in the start year.
        start_year (int): The year corresponding to the initial value.
        end_year (int): The year for which the adjusted value is calculated.
        cpi_data (list of dict): A list of dictionaries containing "Year" and "CPI".

    Returns:
        float: The inflation-adjusted value in the end year.
    """
    # Create a dictionary for quick CPI lookup
    cpi_lookup = {entry["Year"]: entry["CPI"] for entry in cpi_data}

    # Ensure the start and end years exist in the data
    if start_year not in cpi_lookup or end_year not in cpi_lookup:
        raise ValueError("Start year or end year not found in CPI data.")

    # Get the CPI values for the start and end years
    cpi_start = cpi_lookup[start_year]
    cpi_end = cpi_lookup[end_year]

    # Calculate the adjusted value
    adjusted_value = start_value * (cpi_end / cpi_start)
    return adjusted_value


